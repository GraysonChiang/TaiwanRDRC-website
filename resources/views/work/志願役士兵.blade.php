                <li>經中校以上編階主官（管）考核推薦，年齡在32歲以下（自民國74年1月1日以後出生者）徵集入營服常備兵役或服替代役人員(服替代役人員係指於國家安全局、行政院海岸巡防署《組改後為海洋委員會海巡署，以下簡稱海巡署》或國防部所屬機關、部隊服勤者)。</li>
                <li>退伍或常備兵役軍事訓練結訓，年齡在32歲以下（自民國74年1月1日以後出生者）之常備兵後備役、補充兵列管或替代役備役人員。</li>
                <li>年滿18歲至32歲（自民國73年1月1日以後至民國88年12月31日以前出生者）之女子或尚未履行兵役義務之同齡男子。</li>
