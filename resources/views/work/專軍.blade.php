        <div class="col-lg-12 col-xs-12">
            <span style="text-align: center;color: #00A9C6;display: block;"><b>體格</b></span><br>
            <hr style="margin-top: 0px; margin-bottom: 10px;">
            <ol style="list-style-type: disc;line-height: 25px; ">
                <li>男性：<br>
                （１）軍官：身高160公分至195公分，身體質量指數(BMI)為17至31；惟報考陸軍司令部裝甲兵及運輸兵官科人員，身高限165公分以上。身體質量指數(BMI)計算公式：體重(公斤)除以身高(公尺)平方，例：體重80公斤、身高170公分，BMI為「80／(1.7)^2 =27.68」。<br>
                （２）士官：身高152公分至200公分，身體質量指數(BMI)為17至31；惟報考陸軍司令部砲兵、裝甲兵及運輸兵官科人員，身高限165公分以上。<br>
                （３）報考憲兵指揮部及憲兵官科人員，身高限165至187公分，身體質量指數(BMI)：18至30。<br>
                </li>
                <li>女性：<br>
                （１） 軍官：身高155公分至185公分，身體質量指數(BMI)為 17至26；惟報考陸軍司令部運輸兵官科人員，身高限165公分以上。<br>
                （２）士官：身高150公分至200公分，身體質量指數(BMI)為17至26；惟報考陸軍司令部砲兵及運輸兵官科人員，身高限165公分以上。<br>
                （３）報考憲兵指揮部及憲兵官科人員，身高限160至175公分，身體質量指數(BMI)：17至24。<br>
                </li>
                <li>視力
                    <br>最佳矯正視力達0.8以上，且兩眼配鏡總度數各在六屈光度(600度)以內。曾接受眼角膜(雷射)屈光手術6個月以上，檢查無後遺症者。
                </li>
                <li>身體任何部分有刺青（紋身）（報考憲兵男性不得穿耳洞），經體格檢查（以下簡稱體檢）醫院鑑定為不合格者，一律不得報考。</li>
                <li>其他體檢項目須符合「國軍志願役專業預備軍官預備士官班體檢體格區分表」基準（如附表1）。</li>
                <li>體檢採「網路預約」制，考生須依考選簡章指定之醫院、規定期程及事項（如附表2），於「國軍人才招募中心全球資訊網」（http://rdrc.mnd.gov.tw）預約及自費新臺幣1,200元實施體檢，並應於受檢時誠實告知過去病史與隱藏性病症，如刻意隱瞞，於錄取或入學後發現體格不符考選簡章所定基準，則撤銷錄取資格或核予退學。</li>
                <li>考生僅得擇一指定醫院辦理體檢，一經受檢即不得再赴其他醫院辦理體檢，並應妥為保存自費體檢表正本1份，於入學報到時繳交，以利體格資料查驗及建檔使用。</li>
                <li>上開體檢結果1年內有效，並於各梯次以附表1基準審查之。</li>
                <li>役男已實施徵兵體檢，且體格屬「體位區分標準」之「替代役體位」、「免役體位」及「體位未定」者，必須另檢附役男體檢表影本實施複審，未檢附者，體格判定為不合格。</li>
                <li>在營志願役士官及志願士兵報考體檢事宜 <br>
                    由考生所屬單位函請各國軍醫院辦理「國軍志願役專業預備軍官預備士官班體檢」，並至「國軍人才招募中心全球資訊網」辦理預約體檢。
                    <br> 1年內曾做過國軍人員體格分類檢查或年度體檢者，可直接轉載為「國軍志願役專業預備軍官預備士官班體檢」。惟體檢項目不同時，須補做未
                　　　檢項目。
                </li>
                <li>考生體格經「國軍志願役專業預備軍官預備士官考選會」（以下稱考選會）體檢組實施線上及書面體檢複審，鑑定為不符合體格規定者，寄發不合格原因通知單。</li>
            </ol>
