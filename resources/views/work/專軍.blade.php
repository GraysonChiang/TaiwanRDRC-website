        <div class="col-lg-12 col-xs-12">
            <span style="text-align: center;color: #00A9C6;display: block;"><b>體格標準</b></span><br>
            <hr style="margin-top: 0px; margin-bottom: 10px;">
            <ol style="list-style-type: disc;line-height: 25px; ">
                <li>體格應合於「志願士兵選訓實施辦法」第4條第3項第3款規定之「志願士兵體格基準」，（如表）。</li>
                <li>自費體檢表1年內有效，依檢查當日起算至該梯次報名起始日止，並以志願士兵體格基準審查之。</li>
                <li>女子及常備兵後備役人員，應辦理自費體檢並繳交1年內自費體檢表。</li>
                <li>補充兵列管或替代役備役人員，應繳交役男體格檢查表；另前述檢查表已逾1年期限者應附加辦理自費體檢，並繳交1年內自費體檢表。</li>
                <li>
                    徵集入營服常備兵役人員應合於「志願士兵體格基準」，並持1年內役男體格檢查表可辦理轉服；超過1年者，由所屬單位函請各國軍醫院辦理「志願士兵體檢」，並至「國軍人才招募中心全球資訊網」（http://rdrc.mnd.gov.tw）辦理預約體檢。
                </li>
                <li>徵集入營服替代役人員應繳交役男體格檢查表；另前述檢查表已逾1年期限者應附加辦理自費體檢，並繳交1年內自費體檢表。</li>
                <li>
                    自費體檢採「網路預約」制，考生須依甄選簡章指定之醫院、規定期程及事項（如附表3），於「國軍人才招募中心全球資訊網」（http://rdrc.mnd.gov.tw）預約及自費新臺幣1,200元實施體檢，並應於當梯次報名截止前至醫院完成體檢，逾期不受理其報考。
                </li>
                <li>考生僅得擇一指定醫院辦理體檢，一經受檢即不得再赴其他醫院辦理體檢，並應妥為保存體檢表正本1份，於入營報到時繳交，以利體格資料查驗及建檔使用。</li>
                <li>考生體格經「國軍志願士兵甄選會」（以下稱甄選會）體檢組實施體檢表複審，鑑定為不符合體格規定者，寄發不合格原因通知單。</li>
                <li>
                    甄選會將提供考生基本資料向衛生福利部中央健康保險署（簡稱健保署）進行精神疾病病史勾稽事宜；另由健保署提供考生健康（精神疾病就醫）紀錄，必要時考生應配合完成醫療評估作業，俾利依考選簡章所訂基準進行人員甄選之體格查核程序。
                </li>
                <li>
                    曾因精神官能症、精神病、嚴重型憂鬱症、器質性腦徵候群、口吃或啞、性格異常、性心理異常、自閉症、杜瑞氏症、神經性厭食症或暴食症、智能偏低等病症經診斷確定者。或經衛生福利部中央健康保險署進行精神疾病病史勾稽比對符合前述疾病者，判定為不合格或撤銷其錄取資格。
                </li>
                <li>役男徵兵體檢被判定替代役或免役體位確定者，除「身高」、「體格指標值（BMI）」、「血色素濃度」或「扁平足足弓角」等項目符合「志願士兵體格基準」可申請複檢外，其他不合格得直接判定為不合格體位。</li>
            </ol>
