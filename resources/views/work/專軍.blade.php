                <li>士官<br>具中華民國國籍，且無外國國籍，年滿20歲至32歲（74年1月1日以後出生）以內，在國內公立或已立案之私立大專以上學校或教育部認可之國外大專以上學校畢業，符合考選簡章所定資格條件，志願服務軍旅者。 但在營志願士兵、後備役士兵及替代役備役人員，報考年齡為年滿20歲至35歲（71年1月1日以後出生）以內。</li>

