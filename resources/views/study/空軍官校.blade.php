            <span style="text-align: center;color: #00A9C6;display: block;"><b>學校簡介</b></span><br>
            <hr style="margin-top: 0px; margin-bottom: 10px;">
            <ol style="list-style-type: disc;line-height: 25px; ">
                <li>
                    『空軍軍官學校』位於高雄縣岡山鎮，是國內唯一培養飛行員的學校，凱旋門式的白色校門，莊嚴宏偉，右側為典藏我空軍史料之軍史館，左側為廣受民眾好評之軍機展示場。學校區分為行政、教學、生活、運動及機場等五大區域，佔地約五百二十六公頃，為國內首屈一指之優質學府。
                </li>
                <li>
                    本校與其他的軍事院校最大的不同，就是兼具了學校與部隊的特性，在生活硬體設施方面有規劃完善的寢室大樓，校內設置有銀行、郵局、福利社、醫務所、圖書館及學生活動中心…等，在教學設備有「飛機各系統模型教室」、「風洞實驗室」、「電腦實習室」、「發動機教室」、「模擬機教室」…等；運動設施方面有體育館、重量訓練室、多功能休閒館、游泳池及高爾夫球練習場…等，提供學生完善的學習環境。
                </li>
                <li>
                    本校招生班次計有正期學生班（飛行組、專才組）及飛行常備軍官班，正期學生班採行大學航空專業教育，計有航空太空工程、航空機械工程、航空電子工程及航空管理等四個學系，全學程四年，主要課程為大學通識教育、一般必修及專業選修，最少修習128個學分，畢業後授予少尉官階並由教育部授予理工或管理學士學位；飛行常備軍官班招訓對象為大學以上畢業青年，接受二年專業教育及飛行訓練，畢業後授予少尉官階。
                </li>
                <li>
                    本校正期學生班簡單的說即是大家較為熟知的三軍官校四年大學教育班隊，也就是具有高中學資以上且符合軍事學校招生簡章中各項資格者。因應95學年度招生變革（廢除軍校聯招），各軍事學校入學方式改採以教育部大考中心舉辦之『大學學科能力測驗』成績甄選入學，並區分為「學校推薦」及「個人申請」等兩種方式登記入學。
                </li>
                <li>
                    配合教育部大學法的實施，本校在四年的大學教育期間，並沒有實施飛行訓練，僅於三年級暑訓期間，進行最後一次空勤體檢，合格學生在畢業後分班進入飛行訓練；飛行訓練的範疇包括：基本航空理論、飛行前置訓練、航空生理訓練、求生訓練、離心機訓練、基本傘塔訓練、基礎飛行訓練（基本組T-34C型機）與高級飛行訓練（戰鬥組AT-3型機或空運組BH-1900型機）等階段，也就是由最基本的T-34C型螺旋槳教練機開始習飛，經鑑定合格後，再進入到AT-3型噴射教練機或B-1900型空運機高級訓練，飛行訓練階段總共歷時約二年。
                </li>
                <li>本校為培育航太科技人才之搖籃，亦是國內唯一將『航太理論』與『飛行術科』結合為一的航空專業學府。歡迎有志青年一起加入航空事業，拓展豐富您人生的另一片天空。</li>
            </ol>
