            <ol style="list-style-type: disc;line-height: 25px; ">
                <li>
                    陸軍專科學校（Army Academy R.O.C.），簡稱「陸專」，
                    是訓練中華民國國軍士官幹部的軍事學校。
                    位於臺灣桃園市中壢區龍岡地區的軍事學校，早期招收對象在營的國軍士官幹部，
                    近年開始對外招收社會青年、高中／高職生畢業生、中正預校畢業生，並且持續招收在營士官入學，
                    目標是提升中華民國國軍士官幹部的學習能力與學歷。
                </li>
                <li>
                    陸軍專科學校同時亦是各軍種單位委託受訓或專長訓練的軍事學校，
                    該校不僅只協助中華民國陸軍一個軍種受訓與教學，同時協助了海軍陸戰隊、聯勤、憲兵、後備、飛彈指揮部等軍種或單位輔助在校學生修習學業，
                    學生畢業後便回到各自所屬軍種服務。近年更增加政戰局、空軍、電展室、電戰部等軍種單位協助輔導修業
                </li>
                <li>
                    修業年限為二年
                </li>
            </ol>
