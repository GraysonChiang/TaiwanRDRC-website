            <span style="text-align: center;color: #00A9C6;display: block;"><b>校史</b></span><br>
            <hr style="margin-top: 0px; margin-bottom: 10px;">
            <ol style="list-style-type: disc;line-height: 25px; ">
                <li>本校前身為「陸軍第一士官學校」，奉先總統　蔣公之命，創立於民國四十六年二月ㄧ日，初期以召訓部隊優秀士官兵；至民國五十四年開設新制「常備士官班」，招考初中畢業學生，施以兩年比敘高中教育及半年軍事教育。</li>
                <li>民國七十五年納編金門第二士官學校，正名為「陸軍士官學校」，八十二年精進士官學校，將常備士官兩年普通高中教育改制為三年，並開辦士官長正規班。</li>
                <li>民國八十七年七月一日改制綜合高中嗣後於八十九年十月一日正式更名為「國立陸軍高級中學」，並奉命陸續開辦士官軍售英儲班、國軍第二專長技職教育班等班隊，士官之養成、進修、技職等教育體制概已成形。</li>
                <li>民國94年8月1日奉教育部核定升格，正式更名為「陸軍專科學校」，同時將學校教育行政、老師、隊職幹部組建為優質的戰鬥團隊，致力打造陸軍專科學校成為一所蘊含「人文素養」的優質軍事學府，為國家培育更多優秀的幹部與戰士。</li>
            </ol>
