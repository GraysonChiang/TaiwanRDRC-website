            <span style="text-align: center;color: #00A9C6;display: block;"><b>簡史</b></span><br>
            <hr style="margin-top: 0px; margin-bottom: 10px;">
            <ol style="list-style-type: disc;line-height: 25px; ">
                <li>民國肇建後，帝國主義橫行，民國有名無實， 國父孫中山先生乃在民國13年，決心於廣州黃埔創立軍官學校，特任先總統蔣公為首任校長，並頒示「親愛精誠」校訓，同年六月十六日，第一期學生開學典禮，
                    即成為校慶之日，由於創校的校址設於廣州黃埔，故國人習稱為「黃埔軍校」。
                </li>
                <li>
                    因應時局三度遷校，最後復校於高雄鳳山地區，並於民43年起採行四年大學教育，民55年開辦專修學生班、民67年轉辦專科生，民84年招收第一期女學生，民86年並增設大學儲備軍官訓練團，迄今已為國軍培育文武兼備幹部28萬餘人，功勳卓著。
                </li>

            </ol>
