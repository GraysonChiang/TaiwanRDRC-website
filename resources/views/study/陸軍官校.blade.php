        <div class="col-lg-4 col-xs-12">
            <span style="text-align: center;color: #00A9C6;display: block;"><b>優良的傳統制度</b></span><br>
            <hr style="margin-top: 0px; margin-bottom: 10px;">
            <ol style="list-style-type: disc;line-height: 25px; ">
                <li>
                    實習幹部制度：<br>
                    以部隊編組之指揮組織為架構，藉不同層級的職務及實務歷練，磨練各期學生基層領導實務，以培養能思考、能指揮、能領導之能力。
                </li>
                <li>
                    榮譽制度：<br>
                    「不說謊、不欺騙、不偷竊、不縱容」是學生終身信守不渝的榮譽信條，旨在實踐「親愛精誠」校訓與「犧牲、團結、負責」之黃埔精神。
                </li>
                <li>
                    體能晉級制度：<br>
                    為落實終身運動理念，精進學生體能，進而加強體能訓練、以奠定軍事訓練基礎。另以運動實用項目為學生終生運動專長之選修，培養終生運動習慣。
                </li>
                <li>
                    校旗護衛暨指揮權轉移：<br>
                    由應屆畢業生將護衛校旗責任交付後期學弟，代表著黃埔精神與榮譽制度的傳承。學生部隊實習幹部指揮權的轉移，象徵黃埔薪火相傳，亦代表責任、榮譽及先烈理想的代代相傳。
                </li>
            </ol>
