        <div class="col-lg-4 col-xs-12">
            <span style="text-align: center;color: #00A9C6;display: block;"><b>校況介紹</b></span><br>
            <hr style="margin-top: 0px; margin-bottom: 10px;">
            <ol style="list-style-type: disc;line-height: 25px; ">
                <li>
                    校園廣闊，佔地234公頃，山巒起伏；幽靜的黃埔湖，水天一色；綠樹及青翠大草坪，令人心曠神怡。此外，教育、實驗大樓及資圖中心等形成完整的教學區；書城、幽靜宿舍及運動場形成方便舒適的生活區；戰鬥攀岩場、PU跑道田徑場、多座籃球、排球、網球、橄欖球、足球場、兩座游泳池、室內體育館、多功能活動中心均提供學生更多元的活動空間；同時，教師陣容堅強，教學設備新穎，有無反射天線測量系統、GPS衛星接收儀、富立葉紅外光譜儀…等先進教學設備與整體的網路系統，提供便捷的網路服務。
                </li>
            </ol>
