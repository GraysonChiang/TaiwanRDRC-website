<ol style="line-height: 55px; font-size: 25px; list-style-type: disc;" >
    <li>
        <span style="color: #00A9C6">自來水優待付費</span> <br>

        為照顧國軍退除役官兵支領退休俸、贍養金或生活補助費人員之生活，減輕其自來水費之負擔，特訂定本辦法。
    </li>

    <li>
        <span style="color: #00A9C6">實施對象</span> <br>

        持有國防部後備指揮部發給備役軍人眷屬身分證之支領退休俸、贍養金或生活補助費人員。
    </li>
    <li>
        <span style="color: #00A9C6">實施內容</span> <br>
        (1)每月用水在&nbsp;20&nbsp;度以下部分按自來水事業奉准水價&nbsp;5&nbsp;折計收；逾&nbsp;20&nbsp;度部分，不予優待。<br>
        (2)使用自來水申請優待付費，應向當地自來水事業繳驗備役軍人眷屬身分證及戶口名簿，並應登錄軍眷姓名為戶名，其付費之優待，自申請月份起開始。<br>
        (3)使用自來水優待付費以一個供水戶為限，分住數處而符合優待條件者，應擇一個供水戶申請優待。<br>

    </li>
    <li>
        <span style="color: #00A9C6">承辦單位</span> <br>
        各縣市榮民服務處
    </li>

    <li>
